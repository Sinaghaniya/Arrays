public class Array2 {

    public static void main(String[]args){

        int arr[] = {45,66,77};

        System.out.println("Advance Forloop");

        for(int i:arr){
            System.out.println(i);
        }

        System.out.println("Normal Forloop");

        for(int i=0;i<=2;i++){
            System.out.println(arr[i]);
        }
    }
}
